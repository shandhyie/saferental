            <h2 class="lined-heading"><span>Cara Pesan</span></h2>
            <ol>
                <li>Buka website “saferental” di smartphone atau di komputer anda.</li>

                <li>Klik menu “Login” untuk masuk pada akun.</li>

                <li>Klik menu “Tarif Rental” untuk mulai mencari daftar mobil yang tersedia.</li>
                <li>Klik “Lihat Daftar Mobil Selengkapnya” untuk melihat daftar mobil yang tersedia. </li>

                <li>Klik “View” untuk mulai melihat secara detail mobil yang dipilih.</li>

                <li>Isi form pada menu “Resevation”. </li>

                <li>Klik “Book Now” untuk memesan.</li>

                <li>Jika ada pembatalan unit mobil yang telah dipesan atau perubahan jadwal harap diberitahukan lebih dari 1 x 24 jam sebelumnya dan kami akan mentransfer kembali DP customer max 2 x 24 jam dari waktu pembatalan. DP akan hangus jika pembatalan sewa dilakukan kurang dari 1 x 24 jam dari jam penjemputan.</li>

                <li>Customer dapat melakukan blocking mobil dengan membayar DP50,000/ booking.</li>

                <li>Sisa pembayaran dapat dilakukan pada saat mobil yang dipesan diambil ditempat rental.</li>



            </ol>
